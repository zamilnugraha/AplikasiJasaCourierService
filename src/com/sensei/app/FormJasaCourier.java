    private void tambahTanggal() {
        int tambah = 0;
        if (rbtreguler.isSelected()) {
            tambah = 4;
        } else if (rbtexpress.isSelected()) {
            tambah = 2;
        } else if (rbtsds.isSelected()) {
            tambah = 0;
        } else if (rbtons.isSelected()) {
            tambah = 1;
        } else if (rbthds.isSelected()) {
            tambah = 1;
        }
        SimpleDateFormat sdf = new SimpleDateFormat("dd MMM yyyy");
        Date tgl = new Date();
        Calendar calTambah = Calendar.getInstance();
        calTambah.setTime(tgl);
        calTambah.add(Calendar.DAY_OF_MONTH, tambah);
        lbltiba.setText(sdf.format(calTambah.getTime()));
    }

